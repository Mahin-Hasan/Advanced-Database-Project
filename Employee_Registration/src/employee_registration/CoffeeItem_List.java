    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
          if(field_id.getText().trim().equals("") || field_name.getText().trim().equals("") || field_type.getText().trim().equals("") || field_quantity.getText().trim().equals("") || field_size.getText().trim().equals("") || field_price.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this, "complete the forms");
            
        }
        
        else
        {
            try
            {
                int id = Integer.parseInt(field_id.getText().trim());
                Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","HR","hr");
                String sql1 = "select * from coffee_item where items_id ="+id;
                
                PreparedStatement ps = conn.prepareStatement(sql1);
           
                ResultSet rs = ps.executeQuery();
                
                if(rs.next())
                {
                   String sql = "Update coffee_item SET coffee_name =?, coffee_type = ?,quantity = ?, coffee_size = ?, price =? where items_id ="+id;
                   PreparedStatement ps1 = conn.prepareStatement(sql);
                   ps1.setString(1, field_name.getText().trim());
                   ps1.setString(2, field_type.getText().trim());
                   ps1.setString(3, field_quantity.getText().trim());
                   ps1.setString(4, field_size.getText().trim());
                   ps1.setString(5, field_price.getText().trim());
                   ps1.executeUpdate();
                   
                   DefaultTableModel tblmodel = (DefaultTableModel)coffeeitem_table.getModel();
                   tblmodel.setRowCount(0);
                   tableData();
                   JOptionPane.showMessageDialog(null,"Update successful");
                }
                else
                {
                   String sql = "insert into coffee_item (items_id, coffee_name, coffee_type,quantity, coffee_size, price) values (?,?,?,?,?,?)";
                   PreparedStatement ps1 = conn.prepareStatement(sql);
                   ps1.setString(1, field_id.getText().trim());
                   ps1.setString(2, field_name.getText().trim());
                   ps1.setString(3, field_type.getText().trim());
                   ps1.setString(4, field_quantity.getText().trim());
                   ps1.setString(5, field_size.getText().trim());
                   ps1.setString(6, field_price.getText().trim());
                   ps1.executeUpdate();
                   
                   DefaultTableModel tblmodel = (DefaultTableModel)coffeeitem_table.getModel();
                   tblmodel.setRowCount(0);
                   tableData();
                   JOptionPane.showMessageDialog(null,"Update successful");
                }
                field_id.setText("");
                field_name.setText("");
                field_type.setText("");
                field_quantity.setText("");
                field_size.setText("");
                field_price.setText("");
            }
            
            
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex);
            }
           
            
            
            
           
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

