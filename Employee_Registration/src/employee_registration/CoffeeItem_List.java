    public void tableData()
    {
                        try
            {
                Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","hr","hr");
                String sql1 = "select * from coffee_item";
                
                PreparedStatement ps = conn.prepareStatement(sql1);
           
                ResultSet rs = ps.executeQuery();
                
                while(rs.next())
                {
                    String id = String.valueOf(rs.getInt("ITEMS_ID"));
                    String name = rs.getString("COFFEE_NAME");
                    String type = rs.getString("COFFEE_TYPE");
                    String qty = String.valueOf(rs.getInt("QUANTITY"));
                    String category = rs.getString("COFFEE_SIZE");
                    String price = String.valueOf(rs.getInt("PRICE"));
                    
                    String tbData[] = {id,name,type,qty,category,price};
                    DefaultTableModel tblmodel = (DefaultTableModel)coffeeitem_table.getModel();
                    
                    tblmodel.addRow(tbData);
                }
                
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex);
            }
    }

