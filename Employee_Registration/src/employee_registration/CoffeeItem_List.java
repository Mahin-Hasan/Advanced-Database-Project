        //code for delete operation
        //for deletion the row must be selected and then we have to press the button
        
         int row = coffeeitem_table.getSelectedRow();
        String cell = coffeeitem_table.getModel().getValueAt(row,0).toString();
        try
        {
            
            
            Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","hr","hr");
                String sql1 = "Delete from coffee_item where items_id ="+cell ;
                
                PreparedStatement ps = conn.prepareStatement(sql1);
                
                ps.executeQuery();
                JOptionPane.showMessageDialog(null,"Item deleted successfully");
                DefaultTableModel tblmodel = (DefaultTableModel)coffeeitem_table.getModel();
                tblmodel.setRowCount(0);
                tableData();
                
        }
        
